// @lc code=start
class Solution {
public:
    int lengthOfLongestSubstring(string s) {
        unordered_set<char> occur;
        int n = s.size();
        int rk = -1;
        int ans = 0;
        for(int i = 0; i < n; i++)
        {
            // 左指针向右移动一格，移除一个字符
            if(i != 0)
                occur.erase(s[i-1]);
            while(rk + 1 < n && !occur.count(s[rk+1]))
            {
                occur.insert(s[rk+1]);
                rk++;
            }
            ans = max(rk-i+1,ans);
        }
        return ans;
    }
};

